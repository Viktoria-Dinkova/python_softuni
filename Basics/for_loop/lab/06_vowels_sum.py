# Да се напише програма, която чете текст (стринг), въведен от потребителя,
# и изчислява и отпечатва сумата от стойностите на гласните букви според таблицата по-долу:
# буква	    a	e	i	o	u
# стойност	1	2	3	4	5

text = input()
sum=0

for i in text:
    if i == 'a':
        sum += 1
    elif i == 'e':
        sum += 2
    elif i == 'i':
        sum += 3
    elif i == 'o':
        sum += 4
    elif i == 'u':
        sum += 5
print(f'{sum}')


