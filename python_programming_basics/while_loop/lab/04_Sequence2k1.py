# Напишете програма, която чете число n, въведено от потребителя, и отпечатва всички числа  ≤  n от редицата: 1, 3, 7, 15, 31, ….
# Всяко следващо число се изчислява като умножим предишното с 2 и добавим 1

n = int(input())

curr_n = 1

while curr_n <= n:
    print(f'{curr_n}')
    curr_n = 2 * curr_n + 1
